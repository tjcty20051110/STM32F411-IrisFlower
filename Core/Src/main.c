static void PredictCallback(const IrisInput* input, uint8_t cmd)
{
  IrisResult result;
  char buffer[128];
  int len;
  int ret;
  
  (void)cmd;
  
  Serial_Handler_SendString("\r\n[DEBUG] In callback, calling AI_Predict...\r\n");
  
  ret = AI_Inference_Predict(input, &result);
  
  snprintf(buffer, sizeof(buffer), "[DEBUG] AI_Predict returned: %d\r\n", ret);
  Serial_Handler_SendString(buffer);
  
  if (ret == 0) {
    len = snprintf(buffer, sizeof(buffer),
      "Predicted: %s (%.1f%%)\r\n",
      AI_Inference_GetClassName(result.predicted_class),
      result.confidence);
    Serial_Handler_SendString(buffer);
    
    Serial_Handler_SendString("Probabilities:\r\n");
    len = snprintf(buffer, sizeof(buffer),
      "  Setosa: %.1f%%, Versicolor: %.1f%%, Virginica: %.1f%%\r\n",
      result.probabilities[0] * 100.0f,
      result.probabilities[1] * 100.0f,
      result.probabilities[2] * 100.0f);
    Serial_Handler_SendString(buffer);
  } else {
    Serial_Handler_SendString("ERROR: Prediction failed!\r\n");
  }
}
